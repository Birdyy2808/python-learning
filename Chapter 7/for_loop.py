# Used to perform repetetive tasks

# range(num) -> Starts from 0 to (num-1)
for i in range(5):
    print(i)

# range(start,stop) -> starts from start till stop-1
for i in range(1,5):
    print(i)

# range(start,stop,step) -> starts from start till stop-1 and diff is of 5 nums
for i in range(0,100,4):
    print(i)

# For loop with list
list = [1,2,4,5,7,8,9,9]
for i in list:
    print(i)

# For loop for tuple
tuple = (1,2,4,5,7,8,9,9)
for i  in tuple:
    print(i)

# for loop with else
for i in range(5):
    print(i)
else:
    print("hello")
